/**
 * Mock de dados quinzenais de presença e material por obra.
 *
 * INFRA-FUTURA: cada CicloQuinzenal virá de:
 *   - faltas / atrasos → PontoTel API: GET /employees/{id}/time-records?start=&end=
 *     agrupados por período (1-15 e 16-fim do mês)
 *   - material_excedente_valor → Supabase:
 *     SELECT SUM(excedente_valor) FROM pedidos_material
 *     WHERE obra_id = ? AND periodo_inicio = ? AND status != 'negado'
 *   - custo_perdido_pessoal → calculado em runtime: Σ (salario/h × horas_perdidas) por func
 *   - indice → Σ (min_atraso/10 × 0.5 + faltas × 4) / n_funcionarios
 *
 * Hierarquia de agrupamento suportada pelo endpoint:
 *   quinzenal  → cada CicloQuinzenal (granularidade mínima)
 *   semestral  → soma dos 12 ciclos de um semestre + normalização pela base esperada
 *   anual      → soma dos 24 ciclos do ano (ou 2 semestres)
 */

export interface CicloQuinzenal {
  periodo:                  string;  // "YYYY-MM-DD" — 1º dia do ciclo (01 ou 16)
  mes_ano:                  string;  // "YYYY-MM"
  label:                    string;  // "Jan 1-15" | "Jan 16-31"
  faltas:                   number;
  atrasos_eventos:          number;
  atrasos_minutos:          number;
  material_excedente_valor: number;
  custo_perdido_pessoal:    number;
  indice:                   number;
}

export interface ObraPresencaCiclos {
  obra_id:        string;
  obra_nome:      string;
  encarregado:    { id: string; nome: string };
  /**
   * Usados para calcular a base esperada semestral/anual:
   *   horas_esperadas_semestre = n_funcionarios × horas_mensais × 6
   *   custo_hora_medio = media_salarial / horas_mensais
   *
   * INFRA-FUTURA: virá de JOIN com funcionarios WHERE obra_id = ? AND status = 'ativo'
   */
  n_funcionarios: number;
  horas_mensais:  number;  // horas contratadas por funcionário/mês
  media_salarial: number;  // salário médio da equipe desta obra
  ciclos:         CicloQuinzenal[];
}

// ── helpers internos ──────────────────────────────────────────────────────────

function q(periodo: string, mes_ano: string, label: string,
  faltas: number, atrasos_eventos: number, atrasos_minutos: number,
  material_excedente_valor: number, custo_perdido_pessoal: number, indice: number
): CicloQuinzenal {
  return { periodo, mes_ano, label, faltas, atrasos_eventos, atrasos_minutos,
    material_excedente_valor, custo_perdido_pessoal, indice };
}

